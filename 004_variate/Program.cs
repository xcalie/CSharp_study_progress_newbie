namespace _004_variate
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int a = -1;
            Console.WriteLine(a);

            uint b = 1;
            Console.WriteLine(b);

            long c = -1;
            Console.WriteLine(c);

            ulong d = 1;
            Console.WriteLine(d);

            char e = 'a';
            Console.WriteLine(e);

            bool f = true;
            Console.WriteLine(f);

            float g = 1.0f;
            Console.WriteLine(g);

            double h = 1.0;
            Console.WriteLine(h);

            decimal j = 1.0M;
            Console.WriteLine(j);
        }
    }
}
